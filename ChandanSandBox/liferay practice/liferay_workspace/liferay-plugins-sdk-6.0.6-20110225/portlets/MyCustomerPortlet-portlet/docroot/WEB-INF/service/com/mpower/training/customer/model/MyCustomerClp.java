package com.mpower.training.customer.model;

import com.liferay.portal.kernel.bean.AutoEscapeBeanHandler;
import com.liferay.portal.kernel.util.StringBundler;
import com.liferay.portal.model.impl.BaseModelImpl;

import java.io.Serializable;

import java.lang.reflect.Proxy;

/**
 * @author Brian Wing Shun Chan
 */
public class MyCustomerClp extends BaseModelImpl<MyCustomer>
	implements MyCustomer {
	public MyCustomerClp() {
	}

	public long getPrimaryKey() {
		return _id;
	}

	public void setPrimaryKey(long pk) {
		setId(pk);
	}

	public Serializable getPrimaryKeyObj() {
		return new Long(_id);
	}

	public long getId() {
		return _id;
	}

	public void setId(long id) {
		_id = id;
	}

	public String getName() {
		return _name;
	}

	public void setName(String name) {
		_name = name;
	}

	public String getAddress() {
		return _address;
	}

	public void setAddress(String address) {
		_address = address;
	}

	public MyCustomer toEscapedModel() {
		if (isEscapedModel()) {
			return this;
		}
		else {
			return (MyCustomer)Proxy.newProxyInstance(MyCustomer.class.getClassLoader(),
				new Class[] { MyCustomer.class },
				new AutoEscapeBeanHandler(this));
		}
	}

	public Object clone() {
		MyCustomerClp clone = new MyCustomerClp();

		clone.setId(getId());
		clone.setName(getName());
		clone.setAddress(getAddress());

		return clone;
	}

	public int compareTo(MyCustomer myCustomer) {
		int value = 0;

		value = getName().compareTo(myCustomer.getName());

		if (value != 0) {
			return value;
		}

		return 0;
	}

	public boolean equals(Object obj) {
		if (obj == null) {
			return false;
		}

		MyCustomerClp myCustomer = null;

		try {
			myCustomer = (MyCustomerClp)obj;
		}
		catch (ClassCastException cce) {
			return false;
		}

		long pk = myCustomer.getPrimaryKey();

		if (getPrimaryKey() == pk) {
			return true;
		}
		else {
			return false;
		}
	}

	public int hashCode() {
		return (int)getPrimaryKey();
	}

	public String toString() {
		StringBundler sb = new StringBundler(7);

		sb.append("{id=");
		sb.append(getId());
		sb.append(", name=");
		sb.append(getName());
		sb.append(", address=");
		sb.append(getAddress());
		sb.append("}");

		return sb.toString();
	}

	public String toXmlString() {
		StringBundler sb = new StringBundler(13);

		sb.append("<model><model-name>");
		sb.append("com.mpower.training.customer.model.MyCustomer");
		sb.append("</model-name>");

		sb.append(
			"<column><column-name>id</column-name><column-value><![CDATA[");
		sb.append(getId());
		sb.append("]]></column-value></column>");
		sb.append(
			"<column><column-name>name</column-name><column-value><![CDATA[");
		sb.append(getName());
		sb.append("]]></column-value></column>");
		sb.append(
			"<column><column-name>address</column-name><column-value><![CDATA[");
		sb.append(getAddress());
		sb.append("]]></column-value></column>");

		sb.append("</model>");

		return sb.toString();
	}

	private long _id;
	private String _name;
	private String _address;
}